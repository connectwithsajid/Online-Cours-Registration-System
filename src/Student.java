    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect = DriverManager.getConnection("jdbc:derby://localhost:1527/CourseRegistration");
            Statement statement = connect.createStatement();
            String SName = jComboBox1.getSelectedItem().toString();
            
            if(SName == null){
                JOptionPane.showMessageDialog(null, "Please Select the Student Name to be Deleted!!!");
                
            }else{
                String RemoveQuery = "Delete from Student" + " where StudentName = '"+SName+"'";
                statement.executeUpdate(RemoveQuery);
                jComboBox1.removeItem(SName);
                jTable2.removeAll();
                DisplayTable();
            }
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null,"Error while connecting to Database!!!\n" + e.getMessage().toString());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

