    private void AddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddStudentActionPerformed
        // TODO Add Database URL in url
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect = DriverManager.getConnection("jdbc:derby://localhost:1527/CourseRegistration");
            
            String JStudentName = Student_name.getText();
            String JStudentId = Student_Id.getText();
            if(!JStudentName.isEmpty() && !JStudentId.isEmpty()){

                String AddQuery = "Insert into Student values (?,?)";
                PreparedStatement statement = connect.prepareStatement(AddQuery);
                statement.setInt(1,Integer.parseInt(JStudentId));
                statement.setString(2,JStudentName);
                statement.executeUpdate();
                jComboBox1.addItem(JStudentName);
                jTable2.removeAll();
                DisplayTable();

            }else
            if(JStudentName.isEmpty()){
                JOptionPane.showMessageDialog(null,"Please Enter Course Name");
            }else
            if(JStudentId.isEmpty()){
                JOptionPane.showMessageDialog(null,"Please Enter Course Id");
            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(null,"Error while connecting to Database!!!\n" + e.getMessage().toString());
        }
    }//GEN-LAST:event_AddStudentActionPerformed

